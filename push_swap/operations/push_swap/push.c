#include "../../push_swap.h"

void	pa(t_stack *a, t_stack *b)
{
	if (b->size < 1)
		return ;
	push(pop(b), a);
}

void	pb(t_stack *a, t_stack *b)
{
	if (a->size < 1)
		return ;
	push(pop(a), b);
}
