#include "libft.h"

void	ft_bzero(void *s, size_t n)
{
	unsigned char	*_s;
	unsigned int	i;

	_s = (unsigned char *)s;
	i = 0;
	while (i < n)
	{
		_s[i] = 0;
		i++;
	}
}
