char	*ft_strrchr(const char *string, int c)
{
	int	i;

	i = 0;
	while (*(string + i))
		i++;
	while (i >= 0)
	{
		if (*(string + i) == c)
			return (string + i);
		i--;
	}
	return (0);
}
