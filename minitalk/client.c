#include "minitalk.h"

int	main(int ac, char *av[])
{
	pid_t	pid;
	
	if (ac != 3)
	{
		write(1, "ERROR\n", 6);
		return (0);
	}
	pid = atoi(av[1]);
	kill(pid, SIGUSR1);
}